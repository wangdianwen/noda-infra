-- ============================================
-- Noda Monorepo - Keycloak Schema 初始化
-- ============================================
-- 此脚本在容器首次启动时自动执行
-- 创建 Keycloak 专用的独立 schema

-- ============================================
-- 创建 Keycloak schema
-- ============================================
CREATE SCHEMA IF NOT EXISTS keycloak;

-- ============================================
-- 授予权限给 keycloak_user
-- ============================================
GRANT ALL PRIVILEGES ON SCHEMA keycloak TO keycloak_user;

-- ============================================
-- 输出创建结果
-- ============================================
\echo '=========================================='
\echo 'Keycloak schema 初始化完成'
\echo '=========================================='
\echo 'Schema: keycloak'
\echo 'User: keycloak_user'
\echo '权限: ALL PRIVILEGES'
\echo '=========================================='
